int main ()
{
    int num = 2;
    printf("Number: %d\n", num);
    if ( num %2 == 0) {
        printf("The number is even. \n");
    } else {
        printf("The number is odd. \n");
    }

    int digi = 9;
    printf("Digit: %d\n", digi);
    if ( digi %2 == 0) {
        printf("The number is odd. \n");
    } else {
        printf("The number is even. \n");
    }


    return 0;
}